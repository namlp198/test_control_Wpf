    public static MeshGeometry3D CreateExtruded(double depth = 0.2)
    {
        var mesh = new MeshGeometry3D();
        var frontPoints = new List<Point3D>();
        var backPoints = new List<Point3D>();
        foreach (var pt in contour2D)
        {
            frontPoints.Add(new Point3D(pt.X, pt.Y, depth / 2));
            backPoints.Add(new Point3D(pt.X, pt.Y, -depth / 2));
        }
        int n = frontPoints.Count;

        for (int i = 1; i < n - 1; i++)
        {
            mesh.Positions.Add(frontPoints[0]);
            mesh.Positions.Add(frontPoints[i]);
            mesh.Positions.Add(frontPoints[i + 1]);
            int idx = mesh.Positions.Count - 3;
            mesh.TriangleIndices.Add(idx);
            mesh.TriangleIndices.Add(idx + 1);
            mesh.TriangleIndices.Add(idx + 2);
        }

        for (int i = 1; i < n - 1; i++)
        {
            mesh.Positions.Add(backPoints[0]);
            mesh.Positions.Add(backPoints[i + 1]);
            mesh.Positions.Add(backPoints[i]);
            int idx = mesh.Positions.Count - 3;
            mesh.TriangleIndices.Add(idx);
            mesh.TriangleIndices.Add(idx + 1);
            mesh.TriangleIndices.Add(idx + 2);
        }

        for (int i = 0; i < n; i++)
        {
            int j = (i + 1) % n;
            var p1 = frontPoints[i];
            var p2 = frontPoints[j];
            var p3 = backPoints[j];
            var p4 = backPoints[i];
            mesh.Positions.Add(p1);
            mesh.Positions.Add(p2);
            mesh.Positions.Add(p3);
            mesh.Positions.Add(p4);
            int idx = mesh.Positions.Count - 4;
            mesh.TriangleIndices.Add(idx);
            mesh.TriangleIndices.Add(idx + 1);
            mesh.TriangleIndices.Add(idx + 2);
            mesh.TriangleIndices.Add(idx);
            mesh.TriangleIndices.Add(idx + 2);
            mesh.TriangleIndices.Add(idx + 3);
        }

        return mesh;
    }
